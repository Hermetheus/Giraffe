using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace For_Loops
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 5; i++) //3 Conditions
            {
                Console.WriteLine(i);
            }

            Console.ReadLine();
        }
    }
}
